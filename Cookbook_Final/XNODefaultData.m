+(NSString*)soupName {return @"Tomato Soup";}
+(NSString*)soupIngredients
{
    return @"2 Tbs. extra-virgin olive oil\n\
    1 Tbs. unsalted butter\n\
    1 large white onion, finely chopped\n\
    1 large clove garlic, smashed and peeled\n\
    2 Tbs. all-purpose flour\n\
    3 cups lower-salt chicken broth\n\
    28-oz. can whole peeled plum tomatoes, puréed (include the juice)\n\
    1-1/2 tsp. sugar\n\
    1 sprig fresh thyme\n\
    Kosher salt and freshly ground black pepper\n\
    3 Tbs. thinly sliced fresh basil, chives, or dill, or a mixture of all three (omit if using one of the garnishes below)";
}
+(NSString*)soupInstructions
{
    return @"In a nonreactive 5- to 6-quart Dutch oven, heat the oil and butter over medium-low heat until the butter melts. Add the onion and garlic and cook, stirring occasionally, until soft but not browned, about 8 minutes. Add the flour and stir to coat the onion and garlic.\n\n\
    Add the broth, tomatoes, sugar, thyme, and 1/4 tsp. each salt and pepper. Bring to a simmer over medium-high heat while stirring the mixture to make sure that the flour is not sticking to the bottom of the pan. Reduce the heat to low, cover, and simmer for 40 minutes.\n\n\
    Discard the thyme sprig. Let cool briefly and then purée in two or three batches in a blender or food processor. Rinse the pot and return the soup to the pot. Season to taste with salt and pepper. Reheat if necessary. Serve warm but not hot, garnished with the herbs or dolloped with one of the garnishes below.";
}

