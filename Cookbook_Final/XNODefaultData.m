+(NSString*)bakedGoodsName {return @"Multigrain Bread"; }
+(NSString*)bakedGoodsIngredients
{
    return @"For the starter\n\
    1/4 cup cold water\n\
    1/8 tsp. active dry yeast\n\
    1/2 cup bread flour\n\n\
    For the bread dough\n\
    1 cup tepid water\n\
    1/2 tsp. active dry yeast\n\
    1 tsp. granulated sugar\n\
    1-1/2 tsp. sea salt or kosher salt\n\
    2-1/2 cups, plus 2 to 3 Tbs. bread flour\n\
    3/4 cup whole wheat pastry flour\n\
    3 Tbs. hulled pumpkin seeds, very coarsely chopped\n\
    2 Tbs. hulled sunflower seeds\n\
    2 Tbs. millet\n\
    2 Tbs. flax seeds\n\
    1-1/2 Tbs. poppy seeds";
}
+(NSString*)bakedGoodsInstructions
{
    return @"Make the starter\n\
    To make the starter, combine the cold water and the yeast in the bowl of a stand mixer. Stir in the bread flour, cover the bowl with plastic wrap, and let sit overnight at room temperature.\n\n\
    Make the bread\n    The next day, make the dough. Add the tepid water to the starter in the mixer bowl. Stir in the yeast, sugar, salt, and both of the flours and knead on medium-high speed with the dough hook (or the highest speed the mixer will go without walking across the counter) for 6 minutes. (If you don’t have a stand mixer, you can make this bread by hand, kneading it on a lightly floured countertop for 6 minutes.)\n\n\
    Slow the mixer to the lowest setting, add the pumpkin and sunflower seeds, the millet, and the flax and poppy seeds. Knead for a few minutes, until the seeds are completely incorporated through the dough. When ready, the dough should be slightly sticky, but come away from the sides of the bowl. If not, knead in an additional tablespoon or two of flour. Cover the bowl and let rise until doubled, 1-1/2 to 2 hours.\n\n\
    Scrape the dough out onto a lightly floured countertop and knead it into a smooth ball. Put a kitchen towel in a medium-sized bowl and sprinkle the towel with a bit of flour, then put the ball of dough in it, seam side up. Sprinkle the dough with a bit more flour and draw the ends of the kitchen towel up over the dough; let it rise for 1-1/2 hours.\n\n\
    About 15 minutes before you are ready to bake the bread, put a Dutch oven with the lid on it (remove the handle if it's not oven safe) on the lower shelf of the oven and preheat the oven to 450ºF. Have some cornmeal ready, or cut a piece of parchment paper to fit into the bottom of the Dutch oven.\n\n\
    Be very careful from this point on because the pan and cover are incredibly hot, and it’s easy to forget that, especially when you remove the lid and set it aside to put the bread in the pan. Using oven mitts, remove the Dutch oven from the oven and remove the lid. Distribute an even sprinkling of cornmeal over the bottom of the pan. Flip the dough into the pan, turning it over so that the top will land on the bottom of the pan. The best way to do it is to open the towel and pull the edges back as much as you can, so the sides of the bread are completely uncovered, then let the dough drop into the hot pan. If it’s not completely centered, don’t worry about it; it’ll bake up just fine. Using sharp scissors, snip a relatively deep X incision across the top of the bread. Replace the hot lid and put the Dutch oven back in the oven on the lowest shelf. Bake the bread for 30 minutes.\n\n\
    Using oven mitts, carefully remove the pan from the oven, remove the hot lid, and transfer the loaf to a wire rack to cool. If you’re unsure if it’s done, check for doneness with an instant-read thermometer—it should register 190ºF. Cool completely before slicing.";
}

